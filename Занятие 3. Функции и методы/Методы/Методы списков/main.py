numbers = [1, 2, 3]
numbers.append(4)  # Добавление элемента в конец списка
print(numbers)  # [1, 2, 3, 4]


numbers = [3, 1, 4, 2]
numbers.sort()  # Сортировка списка по возрастанию
print(numbers)  # [1, 2, 3, 4]

numbers = [3, 1, 4, 2]
numbers.sort(reverse=True)  # Сортировка списка по убыванию
print(numbers)  # [4, 3, 2, 1]
